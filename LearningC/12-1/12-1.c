#include <stdio.h>

/* 배열이 아닌 문자열을 출력하더라도 배열과 마찬가지로 배열을 시작위치를 가지고 출력함. */
/* -> 컴파일 과정에서 문자열은 첫 번째 문자의 주소로 둔갑(야매)하고 그 이후에는 배열을 출력하는 것과 같은 과정을 거침. */
int main(void)
{
	printf("apple이 저장된 시작 주소값 : %p\n", "apple");
	printf("두 번째 문자의 주소값 : %p\n", "apple" + 1);
	printf("첫 번째 문자 : %c\n", *"apple");				// 간접참조연산 (캐릭터 한 글자 출력)
	printf("두 번째 문자 : %c\n", *("apple" + 1));			// 포인터 연산식 (두번째 캐릭터 문자요소 출력)
	printf("배열로 표현한 세 번째 문자 : %c\n", "apple"[2]);	// 배열 표현식

	return 0;
}